// mobile menu init
function initMobileNav() {
	jQuery('body').mobileNav({
		hideOnClickOutside: true,
		menuActiveClass: 'nav-active',
		menuOpener: '.nav-opener',
		menuDrop: '.main-nav',
		onInit: function(self) {
			var menu = self.drop;
			var links = menu.find('a');

			links.on('click', function() {
				self.hide();
			});
		}
	});
}

